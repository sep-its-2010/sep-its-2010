package sep.conquest.model;

/**
 * The enum class IRSensor represents the 8 IR-sensors of the e-puck robots.
 * 
 * @author Andreas Wilhelm
 *
 */
public enum IRSensor {
	
	/**
	 * The IR-sensor in front of the robot.
	 */
	FRONT,
	
	/**
	 * The IR-sensor in front-right direction of the robot.
	 */
	FRONT_RIGHT,
	
	/**
	 * The IR-sensor on the right-hand side of the robot.
	 */
	RIGHT,
	
	/**
	 * The IR-sensor in rear-right direction of the robot. 
	 */
	REAR_RIGHT,
	
	/**
	 * The IR-sensor in the rear direction of the robot.
	 */
	REAR,
	
	/**
	 * The IR-sensor in the rear-left direction of the robot.
	 */
	REAR_LEFT,
	
	/**
	 * The IR-sensor on the left-hand side of the robot.
	 */
	LEFT,
	
	/**
	 * The IR-sensor in front-left direction of the robot.
	 */
	FRONT_LEFT;

}
