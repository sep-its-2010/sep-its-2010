package sep.conquest.model;

/**
 * The enumeration for driving-commands.
 * 
 * @author Andreas Wilhelm
 */
public enum Drive {
    
    /**
     * Turn left.
     */
    LEFT,
    
    /**
     * Turn right.
     */
    RIGHT,
    
    /**
     * Drive forward.
     */
    FORWARD,
    
    /**
     * Turn around.
     */
    TURN;    
}
